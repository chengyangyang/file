package com.softi.bean;

public class Student {

	int name;
	int	age;
	
	public Student(int name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public int getName() {
		return name;
	}

	public void setName(int name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
